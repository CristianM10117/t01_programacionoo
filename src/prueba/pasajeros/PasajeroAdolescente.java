package prueba.pasajeros;

public class PasajeroAdolescente extends Avion{
	
	String nombre;
	String apellido; 
	String DeportePractica; 
	
	
	public String getNombre() {
		return nombre;
		
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellido() {
		return apellido;
	}
	public void setApellido(String apellido) {
		this.apellido = apellido;
	}
	public String getDeportePractica() {
		return DeportePractica;
	}
	public void setDeportePractica(String deportePractica) {
		DeportePractica = deportePractica;
	}
	


}
